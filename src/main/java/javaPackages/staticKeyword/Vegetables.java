package staticKeyword;

public class Vegetables {
    String name;
    String Color;
    String Size;
    String Vitamin;
    String Nutrition;


    public static void main(String[] args) {


        Vegetables Tomatoes = new Vegetables(); // created an object of the class. that means I instantiated the class.
        Tomatoes.name = "Tomatoes";
        Tomatoes.Color = "Red";
        Tomatoes.Size = "small";
        Tomatoes.Vitamin = "c";
        Tomatoes.Nutrition = "Potassium";

        Vegetables Carrot = new Vegetables(); // created an object of the class. that means I instantiated the class.
        Carrot.name = "Carrot";
        Carrot.Color = "Orange";
        Carrot.Size = "Long";
        Carrot.Vitamin = "K1";
        Carrot.Nutrition = "Potassium";


    }
}


